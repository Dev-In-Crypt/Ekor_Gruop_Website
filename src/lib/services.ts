export const servicesByCategory = (key: ServiceCategoryKey) =>
  services.filter((s) => s.category === key);

export const featuredServices = services.filter((s) => s.featured);

/** Знайти послугу за категорією та лиш-слугом */
export const findService = (categorySlug: string, serviceSlug: string) => {
  const cat = categoryBySlug(categorySlug);
  if (!cat) return null;
  return services.find((s) => s.category === cat.key && s.slug === serviceSlug) ?? null;
};

